package cn.itcast.day02;

public class PlusOperator02 {
  public static void main(String[] args) {
    int i = 20;
    System.out.println(++i);//21
    System.out.println(i);//21
    System.out.println("-------------");
    int j = 30;
    System.out.println(j++);//30
    System.out.println(j);//31
  }
}
